# - shotgun surgery: the opposite of divergent change
#   whenever you make a kind of change, you need to make little changes to a lot of different
#   classes all over the place. Then it is hard to make a change, as you may easily miss one piece
# - divergent change is one class that suffers many kinds of changes
#   shotgun surgery is one change that alters many classes
#   both require re-arrange the code structure
# - use move method, move field to put all parts that relates to a change together into one class
# - you can use inline class to bring a whole bunch of behavior together

