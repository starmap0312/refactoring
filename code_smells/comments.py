# - better coding leads to fewer comments
# - try to refactor the code first before adding comments(most of the time, you will find that
#   you don't need the comments)
# - if you need a comment to explain what a block of code does, use extract method and rename
#   method instead
# - if you need to state some rules about the required state of the system, don't use comment but
#   use introduce assertion instead
# - good time to use comment: when no refactoring can be done
# - comment is a good place to say "why" you did something, not "how" you did it
#   let later programmers know why you choose to do that
